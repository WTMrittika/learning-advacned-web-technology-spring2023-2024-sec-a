function greet(name){
    console.log(`Hello, ${name}!`);
}
greet ("Alice");

//function return
function sum(a, b){
    return a+b;
}
let result = sum(1, 2);
console.log(result);

//Arrow function
let hello = name => console.log(`Hello, ${name}!`);
hello("Mrittika");

//Anonymous function
const hii = function(name){
    console.log(`Hello, ${name}!`);
};
hii("Rayhan");
